package com.example.nesflos.model;

public class Infopago {
    private String pid;
    private String pnombre;
    private String papellido;
    private String pntarjeta;
    private String pfvtarjeta;
    private String pcvv;

    public Infopago() {
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getPnombre() {
        return pnombre;
    }

    public void setPnombre(String pnombre) {
        this.pnombre = pnombre;
    }

    public String getPapellido() {
        return papellido;
    }

    public void setPapellido(String papellido) {
        this.papellido = papellido;
    }

    public String getPntarjeta() {
        return pntarjeta;
    }

    public void setPntarjeta(String pntarjeta) {
        this.pntarjeta = pntarjeta;
    }

    public String getPfvtarjeta() {
        return pfvtarjeta;
    }

    public void setPfvtarjeta(String pfvtarjeta) {
        this.pfvtarjeta = pfvtarjeta;
    }

    public String getPcvv() {
        return pcvv;
    }

    public void setPcvv(String pcvv) {
        this.pcvv = pcvv;
    }
}
