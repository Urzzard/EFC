package com.example.nesflos.model;

public class Usuario {
    private String uid;
    private String uusuario;
    private String ucorreo;
    private String ucontraseña;

    public Usuario() {
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getUusuario() {
        return uusuario;
    }

    public void setUusuario(String uusuario) {
        this.uusuario = uusuario;
    }

    public String getUcorreo() {
        return ucorreo;
    }

    public void setUcorreo(String ucorreo) {
        this.ucorreo = ucorreo;
    }

    public String getUcontraseña() {
        return ucontraseña;
    }

    public void setUcontraseña(String ucontraseña) {
        this.ucontraseña = ucontraseña;
    }
}
